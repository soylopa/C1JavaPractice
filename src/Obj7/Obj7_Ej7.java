package Obj7;

public class Obj7_Ej7 {
    public static void main(String[] args) {
        int i =0;
        int j = 0;
        while (i<3) {
            j=0;
            while (j<5) {
                System.out.println("Item 1 = "+ i + " Item 2 = " + j);
                j++;
            }
            i++;
        }
    }
}
