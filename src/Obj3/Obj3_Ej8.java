package Obj3;

public class Obj3_Ej8 {
    public static void main(String[] args) {
        boolean value1 = true;
        boolean value2 = false;
        System.out.println(" True AND True = " + (value1 && value1) );
        System.out.println(" True AND False = " + (value1 && value2) );
        System.out.println(" False AND False = " + (value2 && value2) );
    }
}
