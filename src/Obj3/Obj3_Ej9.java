package Obj3;

public class Obj3_Ej9 {
    public static void main(String[] args) {
        boolean valueTrue = true;
        boolean valueFalse = false;
        System.out.println("True OR True = " + (valueTrue || valueTrue));
        System.out.println("True OR False = " + (valueTrue || valueFalse));
        System.out.println("False OR False= " + (valueFalse || valueFalse));
    }
}
